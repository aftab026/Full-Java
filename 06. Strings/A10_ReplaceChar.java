// q:- 10. Replacing characters in strings with replace().
public class A10_ReplaceChar {
    public static void main(String[] args) {
        String st = "Replacing string";
        //replace() method, replaces all occurrences of 'g' to 'n'
        String rpl = st.replace("i", "o");
        System.out.println("Replaced : " + rpl);
    }
}
