// q:- 6. Write a program to print even number between 10 and 100 using whil
public class A6_EvenNumber {
    public static void main(String[] args) {
        System.out.println("The even number between 10 and 100 is :" );
        for(int i= 10; i<= 100; i=i+2){
            System.out.println(i);
        }

        
    }
}
